function getCSSColor(variable)
{
    let col = toColorObject(getComputedStyle(document.body).getPropertyValue(variable).trim());
    if (!col) return "0xff00ff";
    return parseInt("0x" + toHex(col.red) + toHex(col.green) + toHex(col.blue), 16);
}

